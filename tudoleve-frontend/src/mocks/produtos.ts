export function formatCurrency(centavos: number): string {
  return (centavos / 100).toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' })
}

export function getStockBadge(estoque: number, minimo: number): { label: string; variant: 'danger' | 'warning' | 'success' } {
  if (estoque === 0) return { label: 'Sem estoque', variant: 'danger' }
  if (estoque <= minimo) return { label: 'Crítico', variant: 'danger' }
  if (estoque <= minimo * 2) return { label: 'Baixo', variant: 'warning' }
  return { label: `${estoque} un.`, variant: 'success' }
}
